// This function takes angle (in radians) as an argument and returns its tangent value. This could also be verified using Trigonometry as Tan(x) = Sin(x)/Cos(x).

#include <iostream> 
#include <math.h> 
using namespace std; 

int main() 
{ 
	double x = 2.3; 
	cout << "Tangent value of x = 2.3: "
		<< tan(x) << endl; 

	return 0; 
} 

/*
Sample Output:

Tangent value of x = 2.3: -1.11921
*/