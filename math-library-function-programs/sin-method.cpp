// This function takes angle (in radian) as an argument and returns its sine value that could be verified using sine curve.

#include <iostream>
#include <math.h> 
using namespace std;

int main()
{
    double x = 2.3; 
    cout << "Sine value of x = 2.3: " << sin(x) << endl; 
    return 0;
}

/*
Sample Output:

Sine value of x = 2.3: 0.745705
*/