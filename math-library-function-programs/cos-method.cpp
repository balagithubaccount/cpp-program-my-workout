// This function takes angle (in radians) as an argument and returns its cosine value that could be verified using cosine curve.

#include <iostream> 
#include <math.h> 
using namespace std; 

int main() 
{ 
	double x = 2.3; 
	cout << "Cosine value of x = 2.3: " << cos(x) << endl; 

	return 0; 
} 

/*
Sample Output:

Cosine value of x = 2.3: -0.666276
*/
